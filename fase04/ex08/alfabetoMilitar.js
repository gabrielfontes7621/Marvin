function alfabetoMilitar(letra) {
if (letra==="A")return "alpha"
 if (letra==="a")return "alpha"
 if (letra==="E")return "echo"
 if (letra==="e")return "echo"
 if (letra==="I")return "india"
 if (letra==="i")return "india"
 if (letra==="o")return "oscar"
 if (letra==="O")return "oscar"
 if (letra==="U")return "uniform"
 if (letra==="u")return "uniform"
 else 
 return false
}
